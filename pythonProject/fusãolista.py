# Crie uma lista com cinco nomes de pessoas, representando a fila de espera
fila_de_espera = ["Alice", "Bob", "Charlie", "David", "Eve"]

# Imprima a fila atual
print("Fila de espera atual:", fila_de_espera)

# Utilize o método pop(0) para remover o primeiro elemento da lista
fila_de_espera.pop(0)

# Imprima a fila atualizada
print("Fila de espera atualizada após remover o primeiro elemento:", fila_de_espera)

# Utilize o método append() para adicionar dois novos nomes à lista
fila_de_espera.append("Frank")
fila_de_espera.append("Grace")

# Imprima a fila atualizada novamente
print("Fila de espera atualizada após adicionar dois novos nomes:", fila_de_espera)
